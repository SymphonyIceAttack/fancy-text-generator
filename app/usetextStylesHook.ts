export function usetextStylesHook(): [
  typeof inputText,
  typeof setInputText,
  typeof copiedIndex,
  typeof copyToClipboard,
] {
  const [inputText, setInputText] = useState("Transform your text");
  const [copiedIndex, setCopiedIndex] = useState<number | null>(null);

  const copyToClipboard = async (text: string, index: number) => {
    try {
      await navigator.clipboard.writeText(text);
      setCopiedIndex(index);
      setTimeout(() => setCopiedIndex(null), 2000);
    } catch (err) {
      console.error("[v0] Failed to copy text:", err);
    }
  };
  return [inputText, setInputText, copiedIndex, copyToClipboard];
}
